package week1day2;

public class NegativeToPositive 
{
	public static void main(String[] args) 
	{
		int num =-40;
		int ans;
		if(num<0) 
		{
			ans = num*-1;
			System.out.println("The negative number "+num+ " is converted to positive number "+ans);
		}
		else {
			System.out.println("The positive number is"+num);
		}
		
	
	}
	
}
